package utn111.pizzeria.modelo.dao;

public class Pizzas {

  private int id;
  private String nombre;

  public Pizzas() {
  }

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public String getNombre() {
    return nombre;
  }

  public void setNombre(String nombre) {
    this.nombre = nombre;
  }
}
